public class PrimitiveDefaults {
    static boolean b;
    static char c;
    static double d;
    static long l;

    public static void main(String[] args) {
      System.out.println("c: " + c);
      System.out.println("d: " + d);
      System.out.println("l: " + l);
      System.out.println("b: " + b);
      
    }

}
