public class PrimitiveInit{
  static boolean b=true;
  static char c='a';
  static double d=2.3;
  static long l=1000;
  static int i=1000;


public static void main(String[] args) {
    System.out.println("c= "+c);
    System.out.println("d= "+d);
    System.out.println("l= "+l);
    System.out.println("b= "+b);
    System.out.println("i= "+i);
}
}
